    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int fila = jTable1.getSelectedRow(); 

        jTextFieldDniClienteT.setText(jTable1.getValueAt(fila,0).toString());
        jTextFieldNombreT.setText(jTable1.getValueAt(fila,1).toString());
        jTextFieldApellidosT.setText(jTable1.getValueAt(fila,2).toString());
        jTextFieldCodPeliculaT.setText(jTable1.getValueAt(fila,3).toString());
        jTextFieldTituloT.setText(jTable1.getValueAt(fila,4).toString());
        jTextFieldFecha_AlquiladaT.setText(jTable1.getValueAt(fila,5).toString());
        jTextFieldFecha_DevueltaT.setText(jTable1.getValueAt(fila,6).toString());
        jTextFieldDniClienteT.setEnabled(false);
        jTextFieldNombreT.setEnabled(false);
        jTextFieldApellidosT.setEnabled(false);
        jTextFieldCodPeliculaT.setEnabled(false);
        jTextFieldTituloT.setEnabled(false);
        jTextFieldFecha_AlquiladaT.setEnabled(false);
        jTextFieldFecha_DevueltaT.setEnabled(false);
        
    }//GEN-LAST:event_jTable1MouseClicked

